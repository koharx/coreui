export const createCustomTheme = (options: Partial<CustomThemeOptions> = {}) => {
  const baseTheme = options.palette?.mode === 'dark' ? darkTheme : lightTheme;
  return createTheme({
    ...baseTheme,
    ...options,
  });
}; 